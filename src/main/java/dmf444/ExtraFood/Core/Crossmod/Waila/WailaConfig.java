
package dmf444.ExtraFood.Core.Crossmod.Waila;


import mcp.mobius.waila.api.IWailaRegistrar;
import dmf444.ExtraFood.Common.blocks.ChocolateCake;
import dmf444.ExtraFood.Common.blocks.tileentity.TileEntityJuiceBlender;

public class WailaConfig {

	public static void callbackRegister(IWailaRegistrar registrar) {
		registrar.registerBodyProvider(new WailaProviderJuiceBlender(), TileEntityJuiceBlender.class);
		registrar.registerNBTProvider(new WailaProviderJuiceBlender(), TileEntityJuiceBlender.class);
		registrar.registerHeadProvider(new WailaProviderEasterEgg(), ChocolateCake.class);
		registrar.registerBodyProvider(new WailaProviderEasterEgg(), ChocolateCake.class);
	}
}

